package genCURD

import (
	"fmt"
	"os"
	"strings"

	"github.com/pancake-lee/pgo/pkg/plogger"
)

// 生成 curd 的 dao 代码
const daoTplPath = "internal/abandonCodeService/data/dao_AbandonCode.go"

// 遍历每个表，以表为单位，生成一个dao代码文件，处理文件头和文件路径
func genDaoCode(
	tblToSvrMap map[string]*Table,
	tplTable *Table,
) error {
	daoTplBytes, err := os.ReadFile(daoTplPath)
	if err != nil {
		return fmt.Errorf("read tpl file failed: %w", err)
	}

	for _, tbl := range tblToSvrMap {
		plogger.Debugf("gen dao code for tbl[%v]", tbl.TblName)

		daoTplCode := string(daoTplBytes)

		daoCodeStr := `// Code generated by tools/genCURD. DO NOT EDIT.` + "\n\n"
		daoCodeStr += daoReplace(daoTplCode, tplTable, tbl)

		daoOutputPath := fmt.Sprintf("./internal/%vService/data/", tbl.ServiceName)
		_ = os.MkdirAll(daoOutputPath, 0755)

		err = os.WriteFile(daoOutputPath+"z_dao_"+tbl.UpperCamelName+".gen.go", []byte(daoCodeStr), 0644)
		if err != nil {
			return fmt.Errorf("write dao code failed: %w", err)
		}
	}

	return nil
}

func daoReplace(codeStr string, tplTable *Table, tbl *Table) string {
	if tbl.PriCol == nil {
		codeStr = markPairTool.ReplaceAll("MARK REMOVE IF NO PRIMARY KEY", codeStr, "")
	} else {
		codeStr = markPairTool.RemoveMarkSelf("MARK REMOVE IF NO PRIMARY KEY", codeStr)
		codeStr = tblPriIdxReplace(codeStr, tplTable, tbl)
		codeStr = tblIdxReplaceDao(codeStr, tplTable, tbl)
	}

	return tblNameReplace(codeStr, tplTable, tbl)
}

// --------------------------------------------------
// dao代码，替换索引相关代码
const idxWhereTmp = `
	if len(%vList) > 0 {
		do = do.Where(q.%v.In(%vList...))
	}
`

func tblIdxReplaceDao(codeStr string, tplTable *Table, tbl *Table) string {
	indexColDef := ""
	indexColWhere := ""
	for _, idx := range tbl.IdxList {
		for _, c := range idx.Fields {
			funcInputName := c.pbFieldName

			indexColDef += fmt.Sprintf("%sList []%s,\n",
				funcInputName, c.ormFieldType)

			indexColWhere += fmt.Sprintf(idxWhereTmp,
				funcInputName, c.ormFieldName, funcInputName)
		}
	}
	codeStr = markPairTool.ReplaceAll(
		"MARK REPLACE IDX COL", codeStr, indexColDef)
	codeStr = markPairTool.ReplaceAll(
		"MARK REPLACE IDX WHERE", codeStr, indexColWhere)

	return codeStr
}

// --------------------------------------------------
// 替换主键相关代码，dao/pb/service都会用到
func tblPriIdxReplace(codeStr string, tplTable *Table, tbl *Table) string {
	codeStr = strings.ReplaceAll(codeStr,
		tplTable.PriCol.ormFieldName, tbl.PriCol.ormFieldName)

	codeStr = strings.ReplaceAll(codeStr,
		tplTable.PriCol.apiFieldName, tbl.PriCol.apiFieldName)

	codeStr = strings.ReplaceAll(codeStr,
		tplTable.PriCol.ormFieldType, tbl.PriCol.ormFieldType)

	return codeStr
}

// 替换表名相关代码，dao/pb/service都会用到
func tblNameReplace(codeStr string, tplTable *Table, tbl *Table) string {
	codeStr = strings.ReplaceAll(codeStr,
		tplTable.TblName, tbl.TblName)

	codeStr = strings.ReplaceAll(codeStr,
		tplTable.HyphenName, tbl.HyphenName)

	codeStr = strings.ReplaceAll(codeStr,
		tplTable.LowerCamelName, tbl.LowerCamelName)

	codeStr = strings.ReplaceAll(codeStr,
		tplTable.UpperCamelName, tbl.UpperCamelName)

	return codeStr
}
